#Use filter function to filter out the elements from a list that are divisible by 3 & 5

li = list(range(0,100))
result = filter(lambda x: x % 3== 0, li) #divisible by 3
print('Divisble by 3:') 
print(list(result))

result = filter(lambda x: x % 5 == 0, li) #divisible by 5
print('Divisble by 5:')
print(list(result))